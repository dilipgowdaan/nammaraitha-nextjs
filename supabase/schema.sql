create or replace function public.place_marketplace_order(
  p_buyer_id bigint,
  p_farmer_id bigint,
  p_product_id bigint,
  p_quantity integer,
  p_payment_reference text default null
)
returns table(order_id bigint, remaining_quantity integer)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_stock integer;
  v_price numeric(10, 2);
  v_unit text;
begin
  if p_quantity <= 0 then
    raise exception 'Quantity must be positive.';
  end if;

  select quantity, discount_price, unit
    into v_stock, v_price, v_unit
  from public.products
  where id = p_product_id
    and farmer_id = p_farmer_id
  for update;

  if not found then
    raise exception 'Product not found.';
  end if;

  if v_stock < p_quantity then
    raise exception 'Insufficient stock available.';
  end if;

  update public.products
  set quantity = quantity - p_quantity
  where id = p_product_id;

  insert into public.orders (
    buyer_id,
    farmer_id,
    product_id,
    quantity,
    status,
    product_price,
    product_unit,
    payment_reference
  )
  values (
    p_buyer_id,
    p_farmer_id,
    p_product_id,
    p_quantity,
    'paid',
    v_price,
    v_unit,
    p_payment_reference
  )
  returning id into order_id;

  remaining_quantity := v_stock - p_quantity;
  return next;
end;
$$;

revoke all on function public.place_marketplace_order(bigint, bigint, bigint, integer, text)
from public, anon, authenticated;

grant execute on function public.place_marketplace_order(bigint, bigint, bigint, integer, text)
to service_role;

notify pgrst, 'reload schema';
