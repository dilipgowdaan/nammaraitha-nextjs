create or replace function public.create_inventory_reservation(
  p_buyer_id bigint,
  p_farmer_id bigint,
  p_product_id bigint,
  p_quantity integer,
  p_ttl_minutes integer default 10
)
returns table(reservation_id bigint, expires_at timestamptz, available_quantity integer)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_stock integer;
  v_reserved integer;
  v_expires_at timestamptz;
begin
  if p_quantity <= 0 then
    raise exception 'Quantity must be positive.';
  end if;

  update public.inventory_reservations as reservation
  set status = 'expired'
  where reservation.status = 'active'
    and reservation.expires_at <= now();

  select quantity
    into v_stock
  from public.products
  where id = p_product_id
    and farmer_id = p_farmer_id
  for update;

  if not found then
    raise exception 'Product not found.';
  end if;

  select coalesce(sum(reservation.quantity), 0)::integer
    into v_reserved
  from public.inventory_reservations as reservation
  where reservation.product_id = p_product_id
    and reservation.status = 'active'
    and reservation.expires_at > now();

  if (v_stock - v_reserved) < p_quantity then
    raise exception 'Insufficient stock available.';
  end if;

  v_expires_at := now() + make_interval(mins => greatest(p_ttl_minutes, 1));

  insert into public.inventory_reservations (buyer_id, farmer_id, product_id, quantity, status, expires_at)
  values (p_buyer_id, p_farmer_id, p_product_id, p_quantity, 'active', v_expires_at)
  returning id into reservation_id;

  expires_at := v_expires_at;
  available_quantity := v_stock - v_reserved - p_quantity;
  return next;
end;
$$;

revoke all on function public.create_inventory_reservation(bigint, bigint, bigint, integer, integer)
from public, anon, authenticated;

grant execute on function public.create_inventory_reservation(bigint, bigint, bigint, integer, integer)
to service_role;

create or replace function public.release_inventory_reservation(
  p_buyer_id bigint,
  p_reservation_id bigint
)
returns void
language plpgsql
security definer
set search_path = public
as $$
begin
  update public.inventory_reservations as reservation
  set status = 'released'
  where reservation.id = p_reservation_id
    and reservation.buyer_id = p_buyer_id
    and reservation.status = 'active';
end;
$$;

