create or replace function public.place_marketplace_order_v2(
  p_buyer_id bigint,
  p_farmer_id bigint,
  p_product_id bigint,
  p_quantity integer,
  p_payment_reference text default null,
  p_reservation_id bigint default null,
  p_delivery_slot text default null
)
returns table(order_id bigint, remaining_quantity integer)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_stock integer;
  v_reserved integer;
  v_price numeric(10, 2);
  v_unit text;
  v_reserved_quantity integer;
begin
  if p_quantity <= 0 then
    raise exception 'Quantity must be positive.';
  end if;

  update public.inventory_reservations as reservation
  set status = 'expired'
  where reservation.status = 'active'
    and reservation.expires_at <= now();

  select quantity, discount_price, unit
    into v_stock, v_price, v_unit
  from public.products
  where id = p_product_id
    and farmer_id = p_farmer_id
  for update;

  if not found then
    raise exception 'Product not found.';
  end if;

  if p_reservation_id is not null then
    select reservation.quantity
      into v_reserved_quantity
    from public.inventory_reservations as reservation
    where reservation.id = p_reservation_id
      and reservation.buyer_id = p_buyer_id
      and reservation.farmer_id = p_farmer_id
      and reservation.product_id = p_product_id
      and reservation.status = 'active'
      and reservation.expires_at > now()
    for update;

    if not found then
      raise exception 'Reservation expired. Please try checkout again.';
    end if;

    if v_reserved_quantity < p_quantity then
      raise exception 'Reserved quantity is lower than requested quantity.';
    end if;
  else
    select coalesce(sum(reservation.quantity), 0)::integer
      into v_reserved
    from public.inventory_reservations as reservation
    where reservation.product_id = p_product_id
      and reservation.status = 'active'
      and reservation.expires_at > now();

    if (v_stock - v_reserved) < p_quantity then
      raise exception 'Insufficient stock available.';
    end if;
  end if;

  if v_stock < p_quantity then
    raise exception 'Insufficient stock available.';
  end if;

  update public.products
  set quantity = quantity - p_quantity
  where id = p_product_id;

  if p_reservation_id is not null then
    update public.inventory_reservations as reservation
    set status = 'converted'
    where reservation.id = p_reservation_id;
  end if;

  insert into public.orders (
    buyer_id,
    farmer_id,
    product_id,
    quantity,
    status,
    product_price,
    product_unit,
    payment_reference,
    delivery_slot,
    tracking_status
  )
  values (
    p_buyer_id,
    p_farmer_id,
    p_product_id,
    p_quantity,
    'paid',
    v_price,
    v_unit,
    p_payment_reference,
    p_delivery_slot,
    'order_placed'
  )
  returning id into order_id;

  insert into public.order_tracking_events (order_id, status, note, actor_id, actor_role)
  values (order_id, 'order_placed', 'Payment captured and order placed.', p_buyer_id, 'buyer');

  remaining_quantity := v_stock - p_quantity;
  return next;
end;
$$;

revoke all on function public.place_marketplace_order_v2(bigint, bigint, bigint, integer, text, bigint, text)
from public, anon, authenticated;

grant execute on function public.place_marketplace_order_v2(bigint, bigint, bigint, integer, text, bigint, text)
to service_role;

create or replace function public.cancel_marketplace_order(
  p_order_id bigint,
  p_farmer_id bigint,
  p_cancelled_by bigint,
  p_cancel_reason text
)
returns table(order_id bigint, cancelled_at timestamptz)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_order record;
  v_cancelled_at timestamptz;
begin
  if length(trim(coalesce(p_cancel_reason, ''))) < 6 then
    raise exception 'Please mention a clear cancellation reason.';
  end if;

  select *
    into v_order
  from public.orders
  where id = p_order_id
    and farmer_id = p_farmer_id
  for update;

  if not found then
    raise exception 'Order not found.';
  end if;

  if v_order.status = 'delivered' then
    raise exception 'Delivered orders cannot be cancelled.';
  end if;

  if v_order.status = 'cancelled' then
    raise exception 'Order is already cancelled.';
  end if;

  update public.products
  set quantity = quantity + v_order.quantity
  where id = v_order.product_id;

  v_cancelled_at := now();

  update public.orders
  set status = 'cancelled',
      tracking_status = 'cancelled',
      tracking_note = trim(p_cancel_reason),
      cancel_reason = trim(p_cancel_reason),
      cancelled_at = v_cancelled_at,
      cancelled_by = p_cancelled_by
  where id = p_order_id;

  insert into public.order_tracking_events (order_id, status, note, actor_id, actor_role, created_at)
  values (p_order_id, 'cancelled', trim(p_cancel_reason), p_cancelled_by, 'farmer', v_cancelled_at);

  order_id := p_order_id;
  cancelled_at := v_cancelled_at;
  return next;
end;
$$;

revoke all on function public.cancel_marketplace_order(bigint, bigint, bigint, text)
from public, anon, authenticated;

grant execute on function public.cancel_marketplace_order(bigint, bigint, bigint, text)
to service_role;

